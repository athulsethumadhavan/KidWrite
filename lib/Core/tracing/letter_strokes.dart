/// Hand-crafted stroke paths (in writing order) for English letters and
/// digits, expressed in a normalized 0..1 square. Each character maps to a
/// list of strokes; each stroke is a polyline the child should trace in one
/// continuous motion.
///
/// Characters not present here (e.g. Malayalam/Hindi/Tamil glyphs) fall back
/// to automatic skeleton extraction — see glyph_skeleton.dart.
class LetterStrokes {
